#include<stdio.h>
/*
Increment and Decrement Operators
*/

int main()
{
    int n1 = 5;
    printf("n1 = 5\n", n1);
    printf("n1++ : %d\n", n1++);
    printf("n1 : %d\n", n1);
    printf("++n1 : %d\n", ++n1);
    printf("n1 : %d\n", n1);
    printf("n1-- : %d\n", n1--);
    printf("n1 : %d\n", n1);
    printf("--n1 : %d\n", --n1);
    printf("n1 : %d", n1);
    getch();
    return 0;
    
}
