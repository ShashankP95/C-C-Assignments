/*
Logical Operators

AND - &&
OR - ||
NOT - !
*/

int main()
{
    int a = 1234, b = 350;
    printf("a = 1234, b = 350\n", a = 1234, b = 350);
    printf("a == 111 : %d\n", a == 111);
    printf("a == 000 && b == 350 : %d\n", a == 000 && b == 350);
    printf("a == 1234 && b == 350 : %d\n", a == 1234 && b == 350);
    printf("a == 1234 || b == 000 : %d\n", a == 1234 || b == 000);
    printf("a == 7654 || b == 987 : %d\n", a == 7654 || b == 987);
    printf("a != 8765 && b != 234 : %d\n", a != 8765 && b != 234);
    getch();
    
    return 0;
}
    
    
