#include<stdio.h>

//Relational Operators

int main()
{
    int a = 3, b = 7;
    
    printf("a > b : %d\n", a > b);
    printf("a < b : %d\n", a < b);
    printf("a >= b : %d\n", a >= b);
    printf("a <= b : %d\n", a <= b);
    printf("a == b : %d\n", a == b);
    printf("a != b : %d\n", a != b);
    getch();
    
    return 0;
}
